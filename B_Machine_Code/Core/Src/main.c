void OLED_ShowChineseB(uint8_t x, uint8_t y, uint8_t index, uint8_t total_chars) {
    uint8_t start_col = index * 16; uint8_t bytes_per_row = total_chars * 2; 
    for (uint8_t page = 0; page < 2; page++) {
        OLED_WriteCmd(0xB0 + y + page); OLED_WriteCmd(((x & 0xF0) >> 4) | 0x10); OLED_WriteCmd(x & 0x0F);
        for (uint8_t col = 0; col < 16; col++) {
            uint8_t actual_col = start_col + col; uint8_t vData = 0;
            for (uint8_t r = 0; r < 8; r++) {
                uint8_t row = page * 8 + r; uint8_t byte_idx = row * bytes_per_row + (actual_col / 8); uint8_t bit_idx = 7 - (actual_col % 8);
                if (bitmap_bytes[byte_idx] & (1 << bit_idx)) vData |= (1 << r);
            }
            OLED_WriteData(vData);
        }
    }
}

int main(void)
{
  HAL_Init();
  SystemClock_Config();

  __HAL_RCC_GPIOA_CLK_ENABLE(); __HAL_RCC_GPIOB_CLK_ENABLE(); __HAL_RCC_USART1_CLK_ENABLE(); 

  MX_GPIO_Init(); MX_USART1_UART_Init();
  
  HAL_Delay(200);
  OLED_Init(); OLED_Clear(); 

  HAL_UART_Receive_IT(&huart1, &b_rx_byte, 1); 

  while (1)
  {
      // 👉 串口保活看门狗：如果硬件触发溢出死锁，自动复位，拯救高负载仿真卡死！
      if (__HAL_UART_GET_FLAG(&huart1, UART_FLAG_ORE)) {
          __HAL_UART_CLEAR_OREFLAG(&huart1);
          HAL_UART_Receive_IT(&huart1, &b_rx_byte, 1);
      }

      if(b_rx_flag) {
          int p1, p2;
          if(sscanf(b_rx_buf, "PWM1:%d,PWM2:%d", &p1, &p2) == 2) {
              pwm1_val = p1; pwm2_val = p2;
          }
          b_rx_flag = 0;
      }

      sprintf(disp_str, "PWM1:%d%%   ", pwm1_val); OLED_ShowString(0, 0, disp_str);
      sprintf(disp_str, "PWM2:%d%%   ", pwm2_val); OLED_ShowString(0, 2, disp_str);

      if(pwm2_val == 0) {
          OLED_ShowChineseB(0, 4, 0, 12); OLED_ShowChineseB(16, 4, 1, 12); 
          OLED_ShowChineseB(32, 4, 2, 12); OLED_ShowChineseB(48, 4, 3, 12);
      } 
      else if(pwm2_val > 0 && pwm2_val < 80) {
          OLED_ShowChineseB(0, 4, 4, 12); OLED_ShowChineseB(16, 4, 5, 12); 
          OLED_ShowChineseB(32, 4, 6, 12); OLED_ShowChineseB(48, 4, 7, 12);
      } 
      else if(pwm2_val >= 80) {
          OLED_ShowChineseB(0, 4, 8, 12); OLED_ShowChineseB(16, 4, 9, 12); 
          OLED_ShowChineseB(32, 4, 10, 12); OLED_ShowChineseB(48, 4, 11, 12);
      }

      if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_5) == GPIO_PIN_RESET) {
          if(key5_state == 0) { key5_state = 1; uint8_t cmd = 'S'; HAL_UART_Transmit(&huart1, &cmd, 1, 100); }
      } else { key5_state = 0; }

      HAL_Delay(50);
  }
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
    if(huart->Instance == USART1) {
        if(b_rx_byte == '\n') { b_rx_buf[b_rx_idx] = '\0'; b_rx_flag = 1; b_rx_idx = 0; } 
        else { if(b_rx_idx < 49) b_rx_buf[b_rx_idx++] = b_rx_byte; }
        HAL_UART_Receive_IT(&huart1, &b_rx_byte, 1);
    }
}

